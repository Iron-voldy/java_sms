    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            jTable1.setEnabled(false);
            jButton7.setEnabled(false);

            int selectedrow = jTable1.getSelectedRow();

            String studentId = String.valueOf(jTable1.getValueAt(selectedrow, 1));
            jTextField2.setText(studentId);

            String subject = String.valueOf(jTable1.getValueAt(selectedrow, 2));
            jTextField3.setText(subject);

            String status = String.valueOf(jTable1.getValueAt(selectedrow, 5));
            jComboBox2.setSelectedItem(status);

            String attentdate = String.valueOf(jTable1.getValueAt(selectedrow, 4));

            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");

            try {
                Date date = dateFormat.parse(attentdate);
                jDateChooser1.setDate(date);
            } catch (ParseException e) {
                e.printStackTrace();
                // Handle parsing exception
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

