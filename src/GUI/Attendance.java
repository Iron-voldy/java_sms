    private void loadattendnce() {
        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `attendance`INNER JOIN `class` ON `attendance`.`ClassId`= `class`.`ClassNo` INNER JOIN `student` ON `student`.`sno`= `attendance`.`StuID` INNER JOIN `subject` ON `class`.`subNo`= `subject`.`Subno` INNER JOIN `teacher` ON `class`.`tNo`= `teacher`.`Tno` ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector();
                v.add(rs.getString("AteendtId"));
                v.add(rs.getString("student.sno"));
                v.add(rs.getString("class.ClassNo"));
                v.add(rs.getString("class.Name"));
                v.add(rs.getString("Date"));
                v.add(rs.getString("Status"));

                model.addRow(v);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

