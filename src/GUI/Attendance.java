    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        String StudentID = jTextField2.getText();
        String ClassID = jTextField3.getText();
        Date Attentdate = jDateChooser1.getDate();
        String status = String.valueOf(jComboBox2.getSelectedItem());

        if (StudentID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Student Id", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (ClassID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Class Id", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Attentdate == null) {
            JOptionPane.showMessageDialog(this, "Please Select Date", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Status", "Error!", JOptionPane.ERROR_MESSAGE);

        } else {

            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            String dobi = date.format(Attentdate);

            try {
                MySQL.execute("INSERT INTO `attendance`(`StuID`, `ClassId`,`Date`,`Status`)"
                        + " VALUES('" + StudentID + "','" + ClassID + "','" + dobi + "','" + status + "')");

                clean();
                JOptionPane.showMessageDialog(this, "Save successfully");
                loadattendnce();

            } catch (Exception e) {

                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

