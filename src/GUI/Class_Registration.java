    private void loadsubjects() {

        try {

            //search
            ResultSet rs = MySQL.execute("SELECT * FROM `subject`");

            Vector v = new Vector();

            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("Name"));
                Submap.put(rs.getString("Name"), rs.getInt("Subno"));

            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(v);
            jComboBox1.setModel(m);

        } catch (Exception e) {

            e.printStackTrace();
        }
    }

    private void loadteachers() {
        String Subject = String.valueOf(jComboBox1.getSelectedItem());

        if (!Subject.equals("Select")) {
            try {

                int subno = Submap.get(Subject);
                //search
                ResultSet rs = MySQL.execute("SELECT * FROM `teacher` WHERE `Subject_id` = '" + subno + "' ");

                Vector v = new Vector();

                v.add("Select");
                while (rs.next()) {
                    v.add(rs.getString("firstname"));
                    teachermap.put(rs.getString("firstname"), rs.getInt("Tno"));

                }
                DefaultComboBoxModel m = new DefaultComboBoxModel(v);
                jComboBox2.setModel(m);

            } catch (Exception e) {

                e.printStackTrace();
            }
        }
    }

    private void classes() {
        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `class`INNER JOIN `subject` ON `class`.`subNo`= `subject`.`Subno` INNER JOIN `teacher` ON `class`.`tNo`= `teacher`.`Tno`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector();
                v.add(rs.getString("ClassNo"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("subject.Name"));
                v.add(rs.getString("teacher.firstname"));
                v.add(rs.getString("Date"));
                v.add(rs.getString("Time"));

                model.addRow(v);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

