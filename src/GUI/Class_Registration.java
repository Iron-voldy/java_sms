    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        String ClassName = jTextField1.getText();
        String Subject = String.valueOf(jComboBox1.getSelectedItem());
        String Teacher = String.valueOf(jComboBox2.getSelectedItem());
        String Day = String.valueOf(jComboBox4.getSelectedItem());
        String Time = String.valueOf(jComboBox3.getSelectedItem());

        if (ClassName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Class Name", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Subject", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Teacher.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Teacher", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Day.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Date", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Time.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Time", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {

            int subno = Submap.get(Subject);
            int teNo = teachermap.get(Teacher);

            try {
                MySQL.execute("INSERT INTO `class`(`Name`, `subNo`, `tNo`,`Date`,`Time`)"
                        + " VALUES('" + ClassName + "','" + subno + "','" + teNo + "','" + Day + "','" + Time + "')");

                clean();
                JOptionPane.showMessageDialog(this, "Save successfully");
                classes();

            } catch (Exception e) {

                System.out.println(e);
            }
        }


    }//GEN-LAST:event_jButton7ActionPerformed

