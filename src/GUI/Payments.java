    private void jLabel6PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jLabel6PropertyChange

        String ClassId = jLabel6.getText();
        String StudentId = jTextField1.getText();
        StringBuilder paymentValues = new StringBuilder();
        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `invoice`"
                + " WHERE `ClassId` = '" + ClassId + "' AND `StudentID`= '" + StudentId + "'");

            double totalPayment = 0.0;
            int rowCount = 0;

            while (rs.next()) {
                double paymentValue = rs.getDouble("Payment");
                totalPayment += paymentValue;
                rowCount++;
            }

            if (rowCount == 0) {

                jLabel13.setText("No Last Payments");
            } else {

                jLabel13.setText(String.valueOf(totalPayment));

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel6PropertyChange

