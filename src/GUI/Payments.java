    private void loadenrolldetails() {
        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `invoice`");

            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector();
                v.add(rs.getString("PaymentID"));
                v.add(rs.getString("StudentID"));
                v.add(rs.getString("ClassId"));
                v.add(rs.getString("Payment"));
                v.add(rs.getString("DueAmount"));
                v.add(rs.getString("Date"));
                model.addRow(v);
                jTable3.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

