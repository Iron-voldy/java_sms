    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed

        String firstname = jTextField5.getText();
        String Lastname = jTextField6.getText();
        String address = jTextField1.getText();
        String Subject = String.valueOf(jComboBox1.getSelectedItem());

        if (firstname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter First Name", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Lastname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Last Name", "Error!", JOptionPane.ERROR_MESSAGE);
        
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Subject", "Error!", JOptionPane.ERROR_MESSAGE);
        

        } else {

            int subno = Submap.get(Subject);

            try {
                MySQL.execute("INSERT INTO `teacher`(`firstname`, `lastname`, `Address`,`Subject_id`)"
                    + " VALUES('" + firstname + "','" + Lastname + "','" + address + "','" + subno + "')");

                clean();
                // stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Save successfully");
                loadusers();

            } catch (Exception e) {

                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed

        int selectedrow = jTable2.getSelectedRow();

       String firstname = jTextField5.getText();
        String Lastname = jTextField6.getText();
        String address = jTextField1.getText();
        String Subject = String.valueOf(jComboBox1.getSelectedItem());

        if (selectedrow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select Row", "Error!", JOptionPane.ERROR_MESSAGE);

        } else if (firstname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter First Name", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Lastname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Last Name", "Error!", JOptionPane.ERROR_MESSAGE);
        
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (Subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Subject", "Error!", JOptionPane.ERROR_MESSAGE);
        

        } else {

            int subno = Submap.get(Subject);
            String id = String.valueOf(jTable2.getValueAt(selectedrow, 0));
            try {

                MySQL.execute("UPDATE `teacher` SET `firstname`='" + firstname + "',`lastname`='" + Lastname + "',`Address`='" + address + "',`Subject_id`='" + subno + "' WHERE `Tno` = '" + id + "'");
                clean();

                JOptionPane.showMessageDialog(this, "Update successfully");
                loadusers();

            } catch (Exception e) {

                System.out.println(e);
            }

        }
    }//GEN-LAST:event_jButton12ActionPerformed

