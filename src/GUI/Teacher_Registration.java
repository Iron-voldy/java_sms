    private void loadsubjects() {
        
        try {
          
           
            //search
            ResultSet rs = MySQL.execute("SELECT * FROM `subject`");

            Vector v = new Vector();

            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("Name"));
                Submap.put(rs.getString("Name"), rs.getInt("Subno"));

            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(v);
            jComboBox1.setModel(m);

        } catch (Exception e) {

            e.printStackTrace();
        }
    }

    private void loadusers() {
        try {

             ResultSet rs = MySQL.execute("SELECT * FROM `teacher`"
                    + "INNER JOIN `subject` ON `teacher`.`Subject_id`= `subject`.`Subno`"
                    + " ORDER BY Tno ASC");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector();
                v.add(rs.getString("Tno"));
                v.add(rs.getString("firstname"));
                v.add(rs.getString("lastname"));
                v.add(rs.getString("Address"));
                v.add(rs.getString("subject.Name"));
                model.addRow(v);
                jTable2.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


